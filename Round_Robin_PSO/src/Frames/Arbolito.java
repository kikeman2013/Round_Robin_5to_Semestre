    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        Nodo[] arrEnc = new Nodo[1];                                       //Se crea un arreglo de clase Nodo para guardar el proceso
        boolean band;                                                       //Se crea un variable bandera
        char n = CBProceso.getSelectedItem().toString().charAt(0);          //Se obtiene el numero de proceso del Combo Box
        band = semilla.buscarNodo(n, arrEnc);                               //Se utiliza la variable bandera para saber si el proceso a eliminar existe

        if (band) {
            semilla.eliminarNodo(arrEnc[0]);                                //Si existe se elimina del arbol,
        }
        CBProceso.removeItemAt(CBProceso.getSelectedIndex());               //del ComboBox
        DefaultTableModel m = (DefaultTableModel) rr.JtableDatos.getModel(); //de las Tablas
        DefaultTableModel l = (DefaultTableModel) rr.jtblAccess.getModel();
        for (int i = 0; i < m.getRowCount(); i++) {
            if (m.getValueAt(i, 0).toString().charAt(0) == n) {
                m.removeRow(i);
                l.removeRow(i);
            }
        }
        for (int j = 0; j < rr.ProcesosEspera.Length(); j++) {
            if (rr.ProcesosEspera.getProceso(j).getNumProceso() == n) {
                rr.ProcesosEspera.RemoveSelect(j);
            }
        }
    }//GEN-LAST:event_BtnEliminarActionPerformed

