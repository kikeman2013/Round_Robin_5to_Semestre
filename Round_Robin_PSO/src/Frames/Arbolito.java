    //Ingresa los datos en el Frame de Control
    public void Ingresar() {
        if (!jtxfTiempoEjecucion.getText().equals("") && !jtxfTiempoLlegada.getText().equals("")) {
            Random r = new Random();
            rr.jspQuantum = this.jspQuantum;  //Asigna el Quanrum em el FC
            rr.jtxfTiempoEjecucion = this.jtxfTiempoEjecucion; //Asigna el Tirmpo de Ejecución em el FC
            rr.jtxfTiempoLlegada = this.jtxfTiempoLlegada; //Asigna el Tiempo de Llegada em el FC
            int n = r.nextInt(50) + 64;     //Asigna el Numero de Proceso
            //Verifica si el nombre ya existia
            for (int j = 0; j < rr.getProcesosEspera().Length(); j++) {
                if (rr.getProcesosEspera().getProceso(j).getNumProceso() == n) {
                    n = r.nextInt(40) + 64;
                };
            }
            //Inserta el Proceso en el arbol
            semilla.insertar(new Proceso((char) n,
                    Integer.parseInt(this.jtxfTiempoLlegada.getText()),
                    Integer.parseInt(this.jtxfTiempoEjecucion.getText()),
                    Integer.parseInt(this.jspQuantum.getValue().toString()),
                    Integer.parseInt(this.jtxfTiempoEjecucion.getText())));
            //Inserta el numero de proceso en el CB para su eliminacion o busqueda
            this.CBProceso.addItem((char) n + "");
            //Ingresa el proceso en el FC
            rr.Ingresar(n);
        } else {
            System.out.println("LLena los Datos");
        }
    }

